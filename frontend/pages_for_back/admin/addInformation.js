function addInformation(name, img, price, art, descript, select, cal, uglev, belok, zhir){

    document.getElementById("articul").value = art;
    document.getElementById("name").value = name;
    document.getElementById("descript").value = descript;
    document.getElementById("price").value = price;
    document.getElementById("img").value = img;
    document.getElementById("cal").value = cal;
    document.getElementById("uglev").value = uglev;
    document.getElementById("belok").value = belok;
    document.getElementById("zhir").value = zhir;
    document.getElementById("sel").value = select;

}
